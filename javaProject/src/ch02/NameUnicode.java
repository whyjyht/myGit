package ch02;

public class NameUnicode {

	public static void main(String[] args) {

		char a = '\uc1a1';
		char b = '\uc7ac';
		char c = '\uc601';
		
		System.out.println("char형(\'\\uc1a1\') : " + a);
		System.out.println("char형(\'\\uc7ac\') : " + b);
		System.out.println("char형(\'\\uc601\') : " + c);
	}

}
